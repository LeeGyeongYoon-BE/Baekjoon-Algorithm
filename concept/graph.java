// 정점(Vertex, Node)와 간선(Edge)으로 이루어진 자료 구조
// 정점은 객체나 데이터, 간선은 정점 간 연결 관계를 나타냄
// 현실 세계의 관계를 모델링할 때 많이 사용 (예: 지도, 소셜 네트워크, 통신망)

// 종류
// 1. 무방향 그래프 : 간선에 방향 없음
// 2. 방향 그래프 : 간선에 방향 있음
// 3. 가중치 그래프 : 간선에 값(거리, 비용 등) 있음
// 4. 비가중치 그래프 : 간선에 값 없음, 단순 연결 정보만 있음
// 5. 사이클 그래프 : 정점이 순환 형태로 연결됨
// 6. 트리 : 사이클 없는 연결 그래프
// 7. 연결 그래프 : 모든 정점이 최소 하나의 경로로 연결됨

// 표현 방법
// 1. 인접 행렬(Adjacency Matrix)
// 2차원 배열 사용
// arr[i][j] = 1 또는 가중치 -> i에서 j로 가는 간선 존재 여부
// 장점 : 특정 간선 존재 여부 확인 O(1)
// 단점 : 정점 수 많으면 메모리 낭비 O(N^2)
int[][] graph = new int[N][N]; // N: 정점 수
graph[0][1] = 1; // 0 -> 1 연결
graph[1][0] = 1; // 무방향이면 반대도 연결
// 2. 인접 리스트(Adjacency List)
// 각 정점마다 연결된 정점 목록 저장
// 메모리 효율적, 간선 순회 O(E)
List<Integer>[] graph = new ArrayList[N];
for(int i=0; i<N; i++) graph[i] = new ArrayList<>();
graph[0].add(1); // 0 -> 1 연결
graph[1].add(0); // 무방향이면 반대도 연결
